<p> Like all things, IMA has a beginning too.....<br>
IMA, also known as International Music Award, was established in 2002 by the record label, Interscope records.<br>
The awards show was founded by Jimmy Lovine and Ted Field, as a love letter to the music industry.<br>
At the time of the awards formation, interscope was starting its golden era with artists like<br> Eminem, Black Eyed Peas and 50 Cent under their name.
